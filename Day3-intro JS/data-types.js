//Numbers
let score = 58;
console.log("Type of score", score);

//String
let playName = "John";
console.log("Type of playName", typeof playName);


// Boolean
let isAdmin = true;
isAdmin = false;
console.log("Type of isAdmin", typeof isAdmin);

// Null
let car = null;
console.log("Type of car", car);
console.log("Type of car", typeof car);


//undefined: Represents unintentional absence of value
let city;
console.log("Type of city", city);
console.log("Type of city", typeof city);